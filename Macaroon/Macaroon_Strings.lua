-- This is the function that loads the proper localizations			
do
	local locale = GetLocale()
	
	for str,value in pairs(M.Strings) do
		if (type(M.Strings[str]) == "table") then
			if (M.Strings[str][locale]) then
				M.Strings[str] = M.Strings[str][locale]
			else	
				M.Strings[str] = M.Strings[str]["enUS"]
			end
		end
	end
	
	local gender = UnitSex("player")
	
	if (gender == 3) then gender = "FEMALE"	else gender = "MALE" end

	for class in pairs(ClassNames) do
		if (ClassNames[class][gender][locale]) then
			M.Strings[class] = ClassNames[class][gender][locale]
		else
			M.Strings[class] = ClassNames[class][gender]["enUS"]
		end
	end		
end